function employeeFilter(input, criteria) {
    data = JSON.parse(input);
    [property, value] = criteria.split('-');
    let num = 0;
    data.forEach(e => {
        if (e[property] === value) {
            console.log(`${num}. ${e.first_name} ${e.last_name} - ${e.email}`);
            num++;
        };
    });
};


